# Quick Setup — Push to GitHub

# 1. Create a new repo on GitHub (github.com/new)
#    Name it: news-chat
#    Don't initialize with README (we already have one)

# 2. Download the files from Claude and put them in a folder, then run:

cd news-chat-app
git init
git add .
git commit -m "feat: news chat app — AI-powered conversational news briefings"
git branch -M main
git remote add origin https://github.com/YOUR_USERNAME/news-chat.git
git push -u origin main

# That's it! Your repo is live.
